/**
 * Get all LCM results for an event, sorted by date.
 */
export function getNoaLCMResults(eventSlug: string): RaceResult[] {
  return (NOA_ALL_RESULTS[eventSlug] || [])
    .filter(r => r.course === 'LCM')
    .sort((a, b) => a.date.localeCompare(b.date));
}

/**
 * Get all results (both courses) for an event, sorted by date.
 */
export function getNoaAllResults(eventSlug: string): RaceResult[] {
  return (NOA_ALL_RESULTS[eventSlug] || [])
    .sort((a, b) => a.date.localeCompare(b.date));
}

/**
 * Get Noa's best LCM time for an event.
 */
export function getNoaBestLCM(eventSlug: string): number | undefined {
  const lcm = getNoaLCMResults(eventSlug);
  if (lcm.length === 0) return undefined;
  return Math.min(...lcm.map(r => r.time));
}

/**
 * Convert results to trajectory points (age vs time).
 * Uses the best time per calendar year for LCM results.
 */
export function getNoaTrajectory(eventSlug: string): Array<{ age: number; timeSeconds: number; date: string }> {
  const results = getNoaLCMResults(eventSlug);
  if (results.length === 0) return [];

  // Group by year, take best time per year
  const byYear = new Map<number, { time: number; date: string }>();
  for (const r of results) {
    const year = parseInt(r.date.slice(0, 4));
    const existing = byYear.get(year);
    if (!existing || r.time < existing.time) {
      byYear.set(year, { time: r.time, date: r.date });
    }
  }

  return Array.from(byYear.entries())
    .map(([year, { time, date }]) => ({
      age: year - NOA_BIRTH_YEAR,
      timeSeconds: time,
      date,
    }))
    .sort((a, b) => a.age - b.age);
}

/**
 * Total number of race results across all events.
 */
export function getNoaTotalRaces(): number {
  return Object.values(NOA_ALL_RESULTS).reduce((sum, results) => sum + results.length, 0);
}
